<script lang="ts">
	import type { LayoutData } from "./$types";
	import { fly } from "svelte/transition";
	import { goto } from "$app/navigation";
	import { page } from "$app/stores";

	import { links } from "$data/links";
	import { externalLink, Metadata, TreeView } from "$lib";
	import { Button, IconButton, ListItem, TextBox } from "fluent-svelte";
	import Navigation from "@fluentui/svg-icons/icons/navigation_24_regular.svg?raw";

	export let data: LayoutData;
	$: ({
		docs,
		pagePath,
		docsPages,
		currentPage = { name: "Overview", path: "" }
	} = data);

	let value = "";
	let searchQuery = "";
	let searchFocused = false;
	let autoSuggestVisible = false;
	let mobileSidebarVisible = false;
	let selection = 0;

	// Name of the current page used in <title>
	$: pageTitle = currentPage.name;

	// Basic search matching for filtering docs pages
	$: searchResults = docsPages.filter(page =>
		page.name
			.toLowerCase()
			.replace(/ /gi, "")
			.includes((searchQuery ?? "").toLowerCase().replace(/ /gi, ""))
	);

	// Determines if the auto-suggest flyout should be shown
	$: if (searchQuery && searchFocused) autoSuggestVisible = true;

	// Since we can't use bind:value and have our clear button work, the best way is through this handler
	const updateSearchQuery = () => (searchQuery = value);

	// Handler for keyboard navigation in the search autocomplete flyout
	const handleSearchKeys = (e: KeyboardEvent) => {
		const { key } = e;
		if (key === "ArrowUp" || key === "ArrowDown") e.preventDefault();
		if (key === "Enter") {
			if (
				searchResults.length > 0 &&
				$page.routeId !==
					`docs${searchResults[selection].path}/${searchResults[selection].slug}`
			)
				goto(
					`/docs${searchResults[selection].path}/${searchResults[selection].slug}`,
					{
						keepfocus: true
					}
				);
		} else if (key === "ArrowDown") {
			selection++;
			if (selection > searchResults.length - 1) selection = 0;
		} else if (key === "ArrowUp") {
			selection--;
			if (selection < 0) selection = searchResults.length - 1;
		}
	};

	// Action for handling clicks outside a DOM node
	const clickOutside = (node: Element, eventHandler: () => boolean) => {
		const handleClick = (event: MouseEvent) => {
			const path = event.composedPath();
			if (!path.includes(node)) eventHandler();
		};

		document.addEventListener("click", handleClick);

		return {
			destroy() {
				document.removeEventListener("click", handleClick);
			}
		};
	};
</script>

<svelte:head>
	<Metadata
		title="Files â€¢ {pageTitle ? `Docs - ${pageTitle}` : 'Docs'}"
		image="docs"
	/>
</svelte:head>

<section class="docs">
	<aside class="sidebar">
		<div class="search">
			<div
				class="autosuggest-wrapper"
				use:clickOutside={() => (autoSuggestVisible = false)}
			>
				<TextBox
					bind:value
					flyoutVisible={autoSuggestVisible}
					on:clear={updateSearchQuery}
					on:focus={() => (searchFocused = true)}
					on:input={updateSearchQuery}
					on:keydown={handleSearchKeys}
					on:search={() => {
						if (
							searchResults.length > 0 &&
							$page.routeId !==
								`docs${searchResults[selection].path}/${searchResults[selection].slug}`
						)
							goto(
								`/docs${searchResults[selection].path}/${searchResults[selection].slug}`,
								{
									keepfocus: true
								}
							);
					}}
					placeholder="Search Documentation"
					type="search"
				/>
				{#if autoSuggestVisible}
					<div class="autosuggest-flyout scroller">
						{#if searchResults.length > 0}
							{#each searchResults as { name, path, slug }, i}
								<ListItem selected={selection === i} href="/docs{path}/{slug}">
									{name}
								</ListItem>
							{/each}
						{:else}
							<ListItem>No results found</ListItem>
						{/if}
					</div>
				{/if}
			</div>
			<hr role="separator" />
		</div>
		<TreeView tree={docs} />
	</aside>
	<article class="page scroller">
		<div class="search-mobile">
			<IconButton
				on:click={() => (mobileSidebarVisible = !mobileSidebarVisible)}
			>
				{@html Navigation}
			</IconButton>
			<div
				class="autosuggest-wrapper"
				use:clickOutside={() => (autoSuggestVisible = false)}
			>
				<TextBox
					bind:value
					flyoutVisible={autoSuggestVisible}
					on:clear={updateSearchQuery}
					on:focus={() => (searchFocused = true)}
					on:input={updateSearchQuery}
					on:keydown={handleSearchKeys}
					on:search={() => {
						if (
							searchResults.length > 0 &&
							$page.routeId !==
								`docs${searchResults[selection].path}/${searchResults[selection].slug}`
						)
							goto(
								`/docs${searchResults[selection].path}/${searchResults[selection].slug}`,
								{
									keepfocus: true
								}
							);
					}}
					placeholder="Search Documentation"
					type="search"
				/>
				{#if autoSuggestVisible}
					<div class="autosuggest-flyout scroller">
						{#if searchResults.length > 0}
							{#each searchResults as { name, path, slug }, i}
								<ListItem selected={selection === i} href="/docs{path}/{slug}">
									{name}
								</ListItem>
							{/each}
						{:else}
							<ListItem>No results found</ListItem>
						{/if}
					</div>
				{/if}
			</div>
		</div>
			<aside class="sidebar-tree" class:visible={mobileSidebarVisible}>
				<TreeView tree={docs} />
			</aside>
		{#key pagePath}
			<div
				class="page-inner markdown-body"
				in:fly|local={{ y: 6, duration: 300, delay: 300 }}
				out:fly|local={{ y: 6, duration: 300 }}
			>
				<header>
					<span>
						{$page.routeId?.replace("docs", "")?.split("/").join(" / ")}
						{$page.routeId === "docs" ? " / overview" : ""}
					</span>
					<div class="header-right">
						<Button
							variant="hyperlink"
							href="https://github.com/{links.github.owner}/{links.github
								.siteRepo}/edit/main/src/routes/docs{currentPage.path ||
								'/index'}.md"
							{...externalLink}
						>
							Edit this page
						</Button>
					</div>
				</header>
				<slot />
			</div>
		{/key}
	</article>
</section>

